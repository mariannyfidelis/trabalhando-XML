
public class Produto {

	public String nome;
	public double preco;
	public String descricao;
	public String categoria;
	
	public Produto(String nome, double preco, String descricao, String categoria) {
		this.nome = nome;
		this.preco = preco;
		this.descricao = descricao;
		this.categoria = categoria;
	}
	
	
}
